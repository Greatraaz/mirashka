	public function blogSingle($slug)
	{
		$json_url = 'https://mirashka.digital/mirashka-blog/wp-json/wp/v2/posts?slug=' . $slug;
		$ch = curl_init($json_url);
		$options = array(
			CURLOPT_RETURNTRANSFER => true,
			CURLOPT_HTTPHEADER => array('Content-type: application/json')
		);
		curl_setopt_array($ch, $options);
		$result =  curl_exec($ch);
		curl_close($ch);
		$data['blog'] = json_decode($result, true);
		if (empty($data['blog'])) {
			redirect('blogs');
		}
		$data['title'] = $data['blog'][0]['title']['rendered'];
		$small = substr($data['blog'][0]['excerpt']['rendered'], 3, 150);
		$data['description'] = $small;
		$data['keywords'] = $data['blog'][0]['title']['rendered'];
		if ($data['blog'] && $data['title'] && $data['description'] && $data['keywords']) {
			$this->view('blogSingle', $data);
		} else {
			redirect('blogs');
		}
	}




	public function blogs()
	{
		$page = $this->input->get('page') ? $this->input->get('page') : 1;
		$json_url = 'https://mirashka.digital/mirashka-blog/wp-json/wp/v2/posts?per_page=12&page=' . $page . '&_embed';
		$ch = curl_init($json_url);
		$options = array(
			CURLOPT_RETURNTRANSFER => true,
			CURLOPT_HTTPHEADER => array('Content-type: application/json')
		);
		curl_setopt_array($ch, $options);
		$result =  curl_exec($ch);
		curl_close($ch);
		$data['blogs'] = json_decode($result, true);

		$url = 'https://mirashka.digital/mirashka-blog/wp-json/wp/v2/posts';
		$headers = get_headers($url, 1);
		$data['total'] = $headers['X-WP-Total'];

		$this->view('blogs', $data);
	}

